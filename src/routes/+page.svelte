<p>
	The process of letterpress printing remained the main technology for setting and reproducing type
	from its invention in the 1450's up into the 1960's. And even today, a lot of the vocabulary we
	use to talk about type references letterpress technology. In the 1960s and 70s, metal typesetting
	was replaced by photo typesetting. A photo type font,was essentially a slide or film reel of
	letter forms that was projected in sequence onto photo paper. Today, a font of type is a .otf or
	.ttf file stored on your computer. These files contain vector information describing the shapes of
	the letter forms, and also information about the space between different combinations of letters.
	So a font is the physical manifestation of type, whether in metal, film or code.
</p>

