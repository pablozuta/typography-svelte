<p>
	In contrast to a font, typeface describes the visual design of a particular set of letter forms.
	Typefaces are massive design projects. To create a fully functioning contemporary typeface, a
	designer must create hundreds and thousands of carefully considered individual drawings.
</p>
<p>
	At minimum, typefaces these days include uppercase letters, lowercase letters, numbers called
	figures, often in several different styles,fractions and mathematical symbols, punctuation and
	special characters, accented characters in both upper and lower case. And ligatures, specially
	drawn combinations of two or more letters. All of these characters must be individually drawn in
	both regular and italic styles. And both of these styles must be drawn in at least three weights.
	Different degrees of boldness. So type faces are time consuming and skill demanding works of
	design. And like other works of design, they're protected as intellectual property. In order to
	use a type face in the piece of design work, you need to have a license to do so. The typefaces
	that come with our operating system or other software have been licensed for us by the software
	company. But most other high quality typefaces must be purchased and downloaded from their
	designer or distributor in order to be used legally.
</p>

<h3>Stroke and Proportion</h3>
<p>
	What really define the character , the look and feel of a typeface on actually use is not it's
	details but some overall qualities of stroke & proportion , and these qualities are what
	typographers pays most attention to when they're are looking at types. The forms of the
	typographic alphabet emerged from scribes' handwriting in the 15th century. So the lines that form
	letters are called strokes and are based on calligraphic marks.
</p>
