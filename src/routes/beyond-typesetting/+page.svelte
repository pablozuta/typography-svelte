	<p>
		Craig Ward uses a mixture of photography and digital software to create exotic type treatments
		that dramatize their subject matter.
	</p>
	<p>
		The Dutch poster designer Mihail Sherman creates spectacular abstracted typography using digital
		software.
	</p>
	<p>
		In a lot of this kind of work type moves from being a flat mark on the page to being a tangible
		thing. And some designers have pushed this to its limit, taking type off the page all together
		and into three dimensional space.
	</p>
	<p>
		This is a series of typographical experiments by the Austrian designer Stefan Sagmeister. The
		conceptual artist Jenny Holzer brings type into space in a different way, by projecting it at
		large scale onto it's surroundings. In her work there's often a fascinating dialog between the
		typography, and its physical environment. Not all of these examples will be relevant to your
		work. But I hope that it will show you the breadth of what's possible with type. And encourage
		you to think beyond the conventions of classical typography.
	</p>
