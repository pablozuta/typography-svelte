	<h4>Technology and Modern Tools</h4>
	<p>
		The technology of reproduction can also inspire typographic experimentation. In the 1990s, the
		American art director David Carson created the distinctive typographic style for Ray Gun
		Magazine by cutting up type and layering it with a Xerox machine. The rough, scrappy aesthetic
		captured the feel of the grunge music documented in the magazine.
	</p>
	<img
		src="https://cdn.monsterchildren.com/wp-content/uploads/2019/12/04065130/CHRIS-A-COVER.jpg"
		alt="ray gun magazine"
	/>
	<p>
		Modern screen based media can be a little rough around the edges, too. And in contemporary art
		and design there's a fascination with digital glitches. These are typographic illustrations for
		Wired magazine by the British typographer Craig Ward.
	</p>
	<p>
		But of course type has its origins in human handwriting. And many typographers have returned to
		the hand as a way to infuse type with human expression.
	</p>
	<div class="grid-2">
		<div class="card">
			<img
				src="https://1.api.artsmia.org/800/94284.jpg"
