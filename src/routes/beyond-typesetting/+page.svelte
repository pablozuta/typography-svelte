	<h4>Breaking the Rules</h4>

	<div class="grid-3">
		<p class="j">
			Beyond breaking rules like this, designers can also manipulate the forms of the letters
			themselves.Armin Hofmann often manipulated or arranged type to create compelling abstract
			forms in his posters.
		</p>
		<p class="j">
			In the 1960s designers like the Spanish American Victor Moscoso used really abstracted type in
			their event posters, demanding that readers spend time actively decoding their messages.
		</p>
		<p class="j">
			And this visual strategy mirrored the dense and opaque style of the psychedelic music that
			these posters were advertising. These days we can directly manipulate the vector lines of type
			in design software.
		</p>
	</div>
