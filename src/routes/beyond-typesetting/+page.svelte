	<h3 class="under">Beyond Typesetting</h3>
	<div class="grid-2">
		<p class="j">
			We can also go beyond conventional typesetting altogether and approach type not as text to be
			typeset but as <em
				>formal material to be composed, manipulated or rendered in any number of ways.</em
			>
			Sometimes, breaking a rule of typesetting in a controlled way can be poetic. In this book cover,
			for instance, the designers have created uneven spaces in the title, arranging the words in a way
			that draws attention to the symmetry between a man and a woman.
		</p>
		<p class="j">
			In this cover for a book on architecture, the title is broken up with stretched dashes,
			turning it into a shape reminiscent of an architectural structure. And in this cover, for a
			book on the British sculptor Antony Gormley, the letters of the title are stacked vertically,
			referencing the formal characteristics of Gormley's vertical steel sculptures.
		</p>
