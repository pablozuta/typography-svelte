			<br />
			<br />
			<p>
				The Swiss designer Niklaus Troxler, for instance, is well known for his posters for jazz
				music events, many of which used hand lettering to capture the loose, improvisational
				character of the jazz music.
			</p>
		</div>

		<div class="card">
