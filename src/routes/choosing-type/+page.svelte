    <h3 class="under">Choosing A Typeface</h3>
    <p>Today there are hundreds and thousands of available typefaces. So the choice of what typeface to use for a project can be overwhelming. There's no magic recipe for choosing type, but in this lecture we'll look at some of the pragmatic considerations that may inform your choice.</p>
    <img src="https://bonfx.com/wp-content/uploads/2011/02/100-best-font-table-graphic.jpg" alt="different typographies poster">
    <p>If you're choosing a typeface to set in text, you may want to start by considering legibility. How easy it is to distinguish one letter or word from the next.</p>
    <p>As I mentioned before serif typefaces are conventionally seen as easier to read and text than sans serifs because the serifs help visually define each baseline.</p>
    <p>But when considering legibility it's also important to keep things in perspective legibility is subjective and the fact is that type faces with familiar forms are easier to read and those with less familiar forms are harder work.</p>
    <p>If you're designing for the web, availability is even more of a complicated issue by default web browsers display type by calling up fonts from the users computer. And most users are only likely to have a handful of so called web safe fonts on their computer.
    A work around for designers is to use type faces hosted externally, either by a free service like <a href="https://fonts.google.com/" target="_blank" rel="noreferrer">Google Fonts</a>  or from a subscription service like Typekit.</p>
    <p>If your choosing more than one type face for a project, you'll want to choose type faces that go together well. And the trick to combining typefaces successfully is to balance contrast with consistency.</p>
    <p>Avoid using typefaces that are stylistically similar , remember that even though two type faces may look quite different at large size, those differences may become invisible in text.</p>
    <p>
        <img src="https://mir-s3-cdn-cf.behance.net/project_modules/disp/fdf85c772032.5600ae9019ea4.png" alt="periodic table of typefaces">
    To establish a through line of consistency, look for type faces with similar proportions start by comparing the x-height of the type faces. Then compare the shapes of the letters like the shapes of the O's and of the A's and G's. For a perfect match, consider choosing two typefaces that were designed specifically to go together.
    The pragmatic issues we've looked at here are only some of the considerations involved in choosing a typeface. The rest has to do with the subjective feel of the typeface, as it's often shaped by its history, uses, and associations.</p>
