	<h3 class="under">Page Space</h3>
	<p>
		A piece of typography is both form, a visual element with shape, texture, figure and ground, and
		direction, and language, an expression of thought with grammar, syntax, and rhythm.
	</p>
	<p>
		As typographers arrange a type and space, we have to consider both the way that space effects
		composition, and the way that it effects meaning. We have to train our eyes both to see space
		and to read space.
	</p>
	
	<p>
		The spatial principle that most directly affects the meaning of text is the principle of
		proximity. We tend to read things that are closer together as related and things that are
		further apart as less related.
	</p>
	
	<p>
		It's really difficult to overstate the importance of this kind of close attention to spacing.
		It's often the difference between clear typography and confusing typography. 
	</p>
	
	<p>
		Indents are used to mark the division between two paragraphs of text. So the first paragraph of
		a body of text with nothing in front of it should not be indented. You can also signal paragraph
		breaks with a white line, a vertical space the size of the letting. But you should never use
		both a white line and an indent because both mean the same thing.
	</p>
	<p>
		Zooming out the same principles of proximity apply to the arrangement of typographical elements
		in a composition, such as a page spread.
	</p>
	
	
	<p>
		It's not easy to create a typographic composition that both
		reads well and looks good. But it's this challenge that makes composing type really rewarding
		and interesting.
	</p>
