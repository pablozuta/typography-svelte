	<p>
		Or in the opposite extreme, we can create a gridded and orderly composition like Armin Hofmann,
		the mid century Swiss designer, has done in this poster about formal principles. 
		</p>
		<img src="https://i0.wp.com/www.printmag.com/wp-content/uploads/2020/06/da4aaf_103b5258f84d4f6e9157f7e7eda035e2mv2.jpeg?resize=763%2C1062&ssl=1" alt="armin hofmann poster black background big letters">
		<p>
		There's no
		formula here for exactly what compositional gesture or what typographic style will best express
		a particular meaning. But that doesn't mean that designers are off the hook. Typography is
		always about communication and a piece of expressive type is only successful if it is able to
		clearly convey its intended message to an audience.
	</p>

