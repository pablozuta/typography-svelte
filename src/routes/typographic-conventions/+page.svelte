	<h3 class="under">Typographic Conventions</h3>
	<p>
		Now that we've looked at the visual principles that govern type setting and typographic
		composition, we're going to switch our attention to the conventions, the rules of proper type
		setting.
	</p>
	<p>
		Like the spatial and organizational principles we're looked at so far, these conventions
		straddle aesthetics and syntax. They're part form, part grammar. We'll start by looking at line
		breaks. Ideally, each line should break in a place that doesn't interrupt its rhythm. If a word
		needs to be broken with a hyphen, it should be broken between syllables and it should never be
		broken into a chunk smaller than three letters long. One or two-letter words shouldn't be left
		hanging at the end of a line because visually our eyes will tend to dissociate them from the
		rest of the text. So they should be dropped down to the next line.
	</p>
	<p>
		Similarly, single words or parts of words shouldn't left on their own at the bottom of a
		paragraph. You can fix this issue by playing with the line breaks in the proceeding lines. Nor
		should single lines of a paragraph be left stranded on their own. The first line of a paragraph
		alone at the bottom of a page is called an orphan. The last line of paragraph alone at the top
		of a page is a widow.
	</p>
	<p>
		As you'll remember the right uneven side of a block of left-aligned text is called a rag. It's
		uneven by nature, but it's most pleasing visually when it's uneven in an even way, like a cone.
	</p>
	<p>
		A rag that is too even is distracting because it's almost straight but not quite. And a rag that
		is to uneven is distracting because it makes distinctive shapes. In a design you can try to
		achieve a good rag by tweaking the hyphenation settings and line length. But usually you'll end
		up as designers say doing the rag, manually creating line breaks with soft returns and hyphens.
		If you are setting justified text, the same rules regarding line breaks apply. In fact, justify
		text is even trickier to line break because of the danger of creating rivers of white space.
	</p>
	<p>
		Besides breaking words to create line breaks, hyphens are also used in compound words like
		left-wing or pre-owned. Other kinds of dashes serve other functions. En dashes are longer than
		hyphens. Their one end long which is half an M. And they're used to signal a range and if you're
		anywhere you might use the word to. For example July to August or six to seven feet. Dashes are
		longer still. One long. These dashes are used like commas to set a phrase apart in text.
	</p>
	<p>
		Dashes are also used to attribute a quote to a speaker. Incorrect use of dashes is one of those
		common typographic errors designers love to point out. Another is the confusion of foot and inch
		marks with quotation marks. As you can see, they are actually quite different. Foot and inch
		marks are wedge shaped. Single and double quotation marks are curved and come in left right
		pairs. In design, there's an option to turn on smart quotes, which automatically puts the
		correct marks around quotations.
	</p>
	<p>
		Most contemporary text typefaces have four different kinds of numbers or figures. The figures
		were used to seeing our lining figures. Like uppercase letters, lining figures start at the
		baseline and end at the cap height metric. A more traditional form of figures are old style
		figures like lowercase letters, these figures have ascenders and descenders. It's easy to
		remember when to use which kind of figures. Lining figures should be used whenever uppercase
		letters are used such as in titles. And old style figures wherever lowercase letters are used
		like in text.
	</p>
	<p>
		Figures also often come in both proportional and tabular versions. Proportional figures are
		spaced like any other character but some characters taking up more space and others less. By
		contrast, tabular figures are mono-spaced, meaning that each figure takes up the same horizontal
		space as the next. Tabular figures are useful in charts and mathematical equations where you
		need the figures to line up vertically.
	</p>
	<p>
		Just as figures come in a version for text and a version for titling so to do capital letters.
		Capital letters meant for setting in text are called small caps. They have the shape of normal
		capitals, but their height is close to the x height of the lowercase. You use small caps
		anywhere all capitals appear in text, such as in acronyms.
	</p>
	<p>
		The exception is peoples' initials, which always appear in uppercase capitals. Another set of
		characters designed to give text a smooth appearance are ligatures. Two letter forms that are
		linked together as one character. Most typefaces include these basic ligatures. But some also
		include others.
	</p>
	<p>
		All of these conventions may seem esoteric. But when combined with the spacial and
		organizational principles we discussed in the last few videos, they'll add a final degree of
		professional polish to your typography.
	</p>
	
