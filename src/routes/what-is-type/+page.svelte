	<h3 class="under">What is Type?</h3>
	<p>
		Well let's start by sorting out two terms that get confused a lot, <strong>
			font and typeface</strong
		>. This might seem like a nit picky distinction, but it's a distinction that actually reflects
		the dual nature of type.
	</p>
	<p>
		On the one hand, type is a tangible thing, a movable kit of pre-designed forms that can be
		shuffled around to make words. And on the other hand, it's something less tangible, a work of
		design that can be represented and re-represented at many sizes and in many formats.
	</p>

	<img
		src="https://res.cloudinary.com/ddkgbgwvp/image/upload/v1668576809/Typography%20Svelte/Screenshot_20221112-121349_Coursera_lasxay.jpg"
		alt="metal letters"
	/>

	<p>
		A font originally referred to one set of metal type. In a <a href="http://www.briarpress.org/typecase/about" target="_blank" rel="noreferrer">California job case</a>, a standard filing
		system for metal typet hat has the letters and other typographic elements arranged according to
		frequency of use, not unlike the way that the keys are arranged on a computer keyboard. Metal
		type obviously can't be made bigger or smaller or italic or bold. So a font of metal type is
		just one type design in one style, in one weight at one size.
	</p>
	<p>
		Movable type like this was first invented in China around 1040 by an inventor named Bi Sheng. It
		was reinvented 400 years later in Germany in the 15th century by Johannes Gutenberg. Gutenberg
		carved his letter forms at actual scale and then cast them in a mixture of metals, mostly lead.
	</p>

	<p>
		The process of letterpress printing remained the main technology for setting and reproducing
		type from its invention in the 1450's up into the 1960's. And even today, a lot of the
		vocabulary we use to talk about type references letterpress technology. In the 1960s and 70s,
		metal typesetting was replaced by photo typesetting. A photo type font,was essentially a slide
		or film reel of letter forms that was projected in sequence onto photo paper. Today, a font of
		type is a .otf or .ttf file stored on your computer. These files contain vector information
		describing the shapes of the letter forms, and also information about the space between
		different combinations of letters. So a font is the physical manifestation of type, whether in
		metal, film or code.
	</p>
    <h4>What is a Typeface?</h4>
	<p>
		In contrast to a font, typeface describes the <strong
			>visual design of a particular set of letter forms.</strong
		>
		Typefaces are massive design projects. To create a fully functioning contemporary typeface, a designer
		must create hundreds and thousands of carefully considered individual drawings.
	</p>
	<img
		src="https://miro.medium.com/max/720/1*sUoGc8L9osQ6cgMuPaalrg.webp"
		alt="universal alphabet poster"
	/>
	<p>
		At minimum, typefaces these days include uppercase letters, lowercase letters, numbers called
		figures, often in several different styles,fractions and mathematical symbols, punctuation and
		special characters, accented characters in both upper and lower case. And ligatures, specially
		drawn combinations of two or more letters. All of these characters must be individually drawn in
		both regular and italic styles. And both of these styles must be drawn in at least three
		weights. Different degrees of boldness. So type faces are time consuming and skill demanding
		works of design. And like other works of design, they're protected as intellectual property. In
		order to use a type face in the piece of design work, you need to have a license to do so. The
		typefaces that come with our operating system or other software have been licensed for us by the
		software company. But most other high quality typefaces must be purchased and downloaded from
		their designer or distributor in order to be used legally.
	</p>

